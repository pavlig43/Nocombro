package ru.pavlig43.core.model


/**
 * Элемент коллекции с уникальным ID для diff-обновления списков в БД.
 *
 * @see ChangeSet Сравнение списков.
 * Используется в ChangeSet<List<T>> → сравнение old/new по [id]:
 * Логика обновления:
 * - **ВСТАВКА**: в старом списке нет элемента с таким id
 * - **ОБНОВЛЕНИЕ**: элемент с id есть, но данные изменились
 * - **УДАЛЕНИЕ**: id есть в старом списке, но нет в новом
 */
interface CollectionObject {
    val id:Int
}